class DraftJDOutput(BaseModel):
    job_description: str
    required_skills: list[str]
    nice_to_have_skills: list[str]
    experience_years: int
    job_type: str
    location: str


class CandidateResult(BaseModel):
    name: str
    matched_skills: list[str]
    unmatched_skills: list[str]
    match_score: float


class ShortlistOutput(BaseModel):
    shortlisted: list[CandidateResult]
    rejected: list[CandidateResult]
    shortlist_threshold_used: float


class InterviewSlot(BaseModel):
    candidate: str
    round: int
    suggested_date: str             # YYYY-MM-DD
    duration_mins: int


class ScheduleInterviewOutput(BaseModel):
    interview_plan: list[InterviewSlot]


class OfferLetterOutput(BaseModel):
    candidate_name: str
    role: str
    department: str
    job_type: str
    location: str
    offer_letter: str               # Full offer letter text generated by Claude


class RecruitmentAgentOutput(BaseModel):
    task_id: str
    action: str
    # "completed" | "failed" | "needs_approval"
    # Agent sets this. Orchestrator (Person 1) reads it and decides
    # whether to pause the graph. Agent never touches state.status directly.
    status: str
    result: (
        DraftJDOutput
        | ShortlistOutput
        | ScheduleInterviewOutput
        | OfferLetterOutput
    )


